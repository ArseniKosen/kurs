<?php
// functions.php - файл с функцией расчёта
function calculateY($x, $a) {
    // Проверяем условия для предотвращения деления на ноль
    if (($a * 2 * $x - 5) == 0) {
        throw new Exception("Ошибка: деление на ноль.");
    }

    // Рассчитываем y
    return (3 * $x + 1) / ($a * 2 * $x - 5);
}